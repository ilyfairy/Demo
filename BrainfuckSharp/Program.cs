    public static string BrainfuckToCsfuck(string brainfuckCode)
    {
        static int CheckCount(ReadOnlySpan<char> chars)
        {
            if (chars.Length == 0)
                return 0;
            var c = chars[0];
            int count = 0;
            for (int i = 0; i < chars.Length; i++)
            {
                if (chars[i] == c)
                {
                    count++;
                }
                else
                {
                    break;
                }
            }
            return count;
        }

        var len = brainfuckCode.Length;
        StringBuilder s = new();
        bool isLastRange = false;
        for (int i = 0; i < len; i++)
        {
            var c = brainfuckCode[i];
            if (i == 0 && c is '~' or '-' or '+')
            {
                var count = CheckCount(brainfuckCode.AsSpan(0, len));
                i += count - 1;
                s.Append(string.Join(' ', Enumerable.Repeat(c, count)));
                s.Append("..");
                isLastRange = true;
                continue;
            }

            if (c is '[')
            {
                s.Append("(..)[");
                isLastRange = false;
            }
            else if (c is ']')
            {
                if (!isLastRange)
                {
                    s.Append("..");
                }
                s.Append(']');
            }
            else if (c is '-')
            {
                var count = CheckCount(brainfuckCode.AsSpan(i, len - i));
                i += count - 1;
                s.Append(string.Join(' ', Enumerable.Repeat(c, count)));
                isLastRange = false;
            }
            else if (c is '+')
            {
                var count = CheckCount(brainfuckCode.AsSpan(i, len - i));
                i += count - 1;
                s.Append(string.Join(' ', Enumerable.Repeat(c, count)));
                isLastRange = false;
            }
            else if (c is '>')
            {
                if (!isLastRange)
                {
                    s.Append("..");
                }
                var count = CheckCount(brainfuckCode.AsSpan(i, len - i));
                i += count - 1;
                s.Append(string.Join("..", Enumerable.Repeat(c, count)));
                isLastRange = false;
            }
            else if (c is '<')
            {
                if (!isLastRange)
                {
                    s.Append("..");
                }
                var count = CheckCount(brainfuckCode.AsSpan(i, len - i));
                i += count - 1;
                s.Append(string.Join("..", Enumerable.Repeat(c, count)));
                isLastRange = false;
            }
            else if (c is '.')
            {
                if (!isLastRange)
                {
                    s.Append("..");
                }
                var count = CheckCount(brainfuckCode.AsSpan(i, len - i));
                i += count - 1;
                s.Append(string.Concat(Enumerable.Repeat('~', count)));
                isLastRange = false;
            }
            else if(c is '\r' or '\n')
            {
                if (i < len && c is '\r' && brainfuckCode[i + 1] is '\n')
                {
                    i++;
                }
                if (!isLastRange)
                {
                    s.Append("..");
                }
                s.Append('\n');
            }
        }
        if (!isLastRange)
        {
            s.Append("..");
        }

        return s.ToString();
    }
