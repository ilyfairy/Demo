//  .   把当前格数值按 ASCII 表输出到终端
//  ,   接受一个字节的输入，将其值存储在数据指针的字节中。
//  [   当指针当前值为 0 时，程序跳转至与之对应的] 之后；否则程序正常执行
//  ]	程序跳转回与之对应的[处


public readonly record struct RangeAction(RangeActionType Type, List<RangeAction>? WhileBlock = null);

public enum RangeActionType
{
    None = 0,
    Increment, Decrement,
    LeftShift, RightShift,
    WhileBlock,
    Output,
}


public unsafe class Range : ICloneable, IDisposable
{
    public byte[] Memory { get; private set; } = [];
    public int Pointer { get; private set; }

    public List<RangeAction> Actions { get; private set; } = new();

    public Range this[Range whileBlock]
    {
        get
        {
            Actions.Add(new(RangeActionType.WhileBlock, [.. whileBlock.Actions]));
            return this;
        }
    }

    public Range(Index start, Index end)
    {
        if (start.Actions.Count != 0 || end.Actions.Count != 0)
        {
            Actions.AddRange(start.Actions);
            Actions.AddRange(end.Actions);
        }
        Start = start;
        End = end;
    }

    public void Run()
    {
        if (Memory.Length == 0)
        {
            Memory = new byte[1024];
        }
        foreach (var action in Actions)
        {
            Run(action);
        }
    }

    private void Run(RangeAction action)
    {
        switch (action.Type)
        {
            case RangeActionType.Increment:
                Memory[Pointer]++;
                break;
            case RangeActionType.Decrement:
                Memory[Pointer]--;
                break;
            case RangeActionType.LeftShift:
                Pointer--;
                break;
            case RangeActionType.RightShift:
                Pointer++;
                break;
            case RangeActionType.Output:
                Console.Write((char)Memory[Pointer]);
                break;
            case RangeActionType.WhileBlock:
                while (true)
                {
                    if (Memory[Pointer] == 0)
                        break;
                    foreach (var subAction in action.WhileBlock!)
                    {
                        Run(subAction);
                    }
                }
                break;
            default:
                break;
        }
    }

    public static implicit operator Index(Range range)
        => new Index(range.Actions.ToList());

    public static Range operator &(byte[] left, Range right)
    {
        right.Memory = left;
        return right;
    }

    public static Range operator ~(Range range)
    {
        range.Actions.Insert(0, new(RangeActionType.Output));
        return range;
    }

    public static Range operator <(Range left, Range right)
    {
        left = left.Clone();
        left.Actions.Add(new(RangeActionType.LeftShift));
        left.Actions.AddRange(right.Actions);
        return left;
    }

    public static Range operator >(Range left, Range right)
    {
        left = left.Clone();
        left.Actions.Add(new(RangeActionType.RightShift));
        left.Actions.AddRange(right.Actions);
        return left;
    }

    public static Range operator |(Range left, Range right)
    {
        left = left.Clone();
        left.Actions.AddRange(right.Actions);
        return left;
    }

    public static Range operator +(Range range)
    {
        range.Actions.Insert(0, new(RangeActionType.Increment));
        return range;
    }

    public static Range operator -(Range range)
    {
        range.Actions.Insert(0, new(RangeActionType.Decrement));
        return range;
    }

    public static Range operator +(Range left, Range right)
    {
        left = left.Clone();
        left.Actions.Add(new(RangeActionType.Increment));
        left.Actions.AddRange(right.Actions);
        return left;
    }

    public Index Start { get; }

    public Index End { get; }

    public override string ToString()
    {
        StringBuilder s = new();
        foreach (var action in Actions)
        {
            AppendString(action);
        }

        void AppendString(RangeAction action)
        {
            switch (action.Type)
            {
                case RangeActionType.None:
                    break;
                case RangeActionType.Increment:
                    s.Append('+');
                    break;
                case RangeActionType.Decrement:
                    s.Append('-');
                    break;
                case RangeActionType.LeftShift:
                    s.Append('<');
                    break;
                case RangeActionType.RightShift:
                    s.Append('>');
                    break;
                case RangeActionType.WhileBlock:
                    s.Append('[');
                    foreach (var subAction in action.WhileBlock!)
                    {
                        AppendString(subAction);
                    }
                    s.Append(']');
                    break;
                case RangeActionType.Output:
                    s.Append('.');
                    break;
                default:
                    break;
            }
        }

        return s.ToString();
    }

    public void Dispose()
    {
        Run();
    }

    public Range Clone()
    {
        var range = (Range)MemberwiseClone();
        range.Actions = range.Actions.ToList();
        return range;
    }

    object ICloneable.Clone() => Clone();
}

public class Index
{
    public List<RangeAction> Actions { get; private set; } = new();

    public Index(List<RangeAction> rangeActions)
    {
        Actions = rangeActions;
    }

    public Index(int value, bool fromEnd = false)
    {
    }

}
